# lista valores
print("\n--- LISTA DE VALORES ---")
for x in range(5):
    print(x)

# Lista nomes
lista = ["João","Pedro","Júlia","Henry","Marcos","Bella"]
print("\n--- LISTA DE NOMES ---")
for nome in lista:
    print(nome)
    
# Acumula valores
print("\n--- LISTA ACUMULATIVA ---")
num = [1, 2, 3, 4, 5, 6]
soma = 0
for x in num:
    soma = soma + x
    print(soma)

